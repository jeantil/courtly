var proxyHandler = function proxyHandler(){
    var httpProxy = require('http-proxy');
    var proxy = new httpProxy.RoutingProxy();
    return function proxyHandler(req, res){
        var buffer = httpProxy.buffer(req);
        setTimeout(function () {
            proxy.proxyRequest(req, res, {
                port: 9000,
                host: 'localhost',
                buffer: buffer
            });
        }, 200);
    };
};
